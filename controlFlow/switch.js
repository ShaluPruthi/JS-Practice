const month = "Mar"

switch (month) {
    case "Jan":
        console.log("January");
        
        break;
    case "Feb":
        console.log("Febuary");
        
        break;
    case "Mar":
        console.log("March");
        
        break;
    case "Apr":
        console.log("April");
        
        break;
    case "M":
        console.log("May");
        
        break;
    case "Jun":
        console.log("June");
        
        break;

    default:
        console.log("Default Match");
        
        break;
}